let age = "pAck"; // This is a string literal assigned to the variable 'age'
let isStudent = true; // This is a boolean literal assigned to the variable 'isStudent'
let score = 95; // This is a numeric literal assigned to the variable 'score'
let nullValue = null; // This is a null literal assigned to the variable 'nullValue'
let undefinedValue; // This is an undefined literal assigned to the variable 'undefinedValue'

//typeof -Operator to check the type of literals
console.log(typeof age); // Output: string
console.log(typeof isStudent); // Output: boolean
console.log(typeof score); // Output: number
console.log(typeof nullValue); // Output: object (this is a quirk in JavaScript)
console.log(typeof undefinedValue); // Output: undefined